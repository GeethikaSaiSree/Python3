# Integer variables
num1 = float(input("enter first number: "))
num2 = float(input("enter second number: "))
print(num1, num2)
# Integer Division
result1 = num1 // num2
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Absolute Value
result3 = abs(-100)
print("Absolute Value:", result3)